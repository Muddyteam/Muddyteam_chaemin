package com.example.muddyteam_chaemin.model;

public class WeatherData {
    private String regId;
    private String tmSt;
    private String tmEd;
    private String regSp;
    private String regName;

    // Getters and Setters

    public String getRegId() {
        return regId;
    }

    public void setRegId(String regId) {
        this.regId = regId;
    }

    public String getTmSt() {
        return tmSt;
    }

    public void setTmSt(String tmSt) {
        this.tmSt = tmSt;
    }

    public String getTmEd() {
        return tmEd;
    }

    public void setTmEd(String tmEd) {
        this.tmEd = tmEd;
    }

    public String getRegSp() {
        return regSp;
    }

    public void setRegSp(String regSp) {
        this.regSp = regSp;
    }

    public String getRegName() {
        return regName;
    }

    public void setRegName(String regName) {
        this.regName = regName;
    }
}
